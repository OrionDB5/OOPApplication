    private void jButtonRicercaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonRicercaActionPerformed
        Set<Prenotazione> prenotazioni = db.ricercaPrenotazione(u);
            int j = 1;
            int i = 0;
            for(Prenotazione p : prenotazioni) {
                int numero = p.getFasciaOraria();
                if(i < prenotazioni.size()) {
                    jTable1.setValueAt(p.getCodicePrenotazione(), i, j);
                    j++;
                    jTable1.setValueAt(p.getNumPosto(), i, j);
                    j++;
                    try {
                        jTable1.setValueAt(FasciaOraria.getFasciaOraria(numero), i, j);
                    } catch (FasciaNonValidaException ex) {
                        Logger.getLogger(GUI_RimozionePrenotazione.class.getName()).log(Level.SEVERE, null, ex);
                    }
                }
                j = 1;
                i++;
            }
    }//GEN-LAST:event_jButtonRicercaActionPerformed

    private void jButtonCancellaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonCancellaActionPerformed
        
        boolean cancellata = false;
        if(jTextField1.getText().matches("Prenotazione 1")) {
            if(jTable1.getValueAt(0,1) != null) {
                try {
                    cancellata = db.rimuoviPrenotazione((Integer)jTable1.getValueAt(0,1));
                } catch (PostoNonPresenteException ex) {
                    Logger.getLogger(GUI_RimozionePrenotazione.class.getName()).log(Level.SEVERE, null, ex);
                } catch (FasciaNonValidaException ex) {
                    Logger.getLogger(GUI_RimozionePrenotazione.class.getName()).log(Level.SEVERE, null, ex);
                }
                jTable1.setValueAt(null,0,1);
                jTable1.setValueAt(null,0,2);
                jTable1.setValueAt(null,0,3);
            }
            else {
                try {
                    cancellata = db.rimuoviPrenotazione((Integer)jTable1.getValueAt(1,1));
                } catch (PostoNonPresenteException ex) {
                    Logger.getLogger(GUI_RimozionePrenotazione.class.getName()).log(Level.SEVERE, null, ex);
                } catch (FasciaNonValidaException ex) {
                    Logger.getLogger(GUI_RimozionePrenotazione.class.getName()).log(Level.SEVERE, null, ex);
                }
                jTable1.setValueAt(null,0,1);
                jTable1.setValueAt(null,0,2);
                jTable1.setValueAt(null,0,3);
            }
        }
        else if(jTextField1.getText().matches("Prenotazione 2")) {
            
            try {
                cancellata = db.rimuoviPrenotazione((Integer)jTable1.getValueAt(1,1));
            } catch (PostoNonPresenteException ex) {
                Logger.getLogger(GUI_RimozionePrenotazione.class.getName()).log(Level.SEVERE, null, ex);
            } catch (FasciaNonValidaException ex) {
                Logger.getLogger(GUI_RimozionePrenotazione.class.getName()).log(Level.SEVERE, null, ex);
            }
            jTable1.setValueAt(null,1,1);
            jTable1.setValueAt(null,1,2);
            jTable1.setValueAt(null,1,3);
        }
        
        if(cancellata)
            jTextArea1.setText("Prenotazione cancellata");
        else
            jTextArea1.setText("Prenotazione non cancellata");
    }//GEN-LAST:event_jButtonCancellaActionPerformed

