    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if(jTable1.getValueAt(0,1) != null || jTable1.getValueAt(1,1) != null) {
            BufferedWriter bw;
            try {
                bw = new BufferedWriter(new FileWriter("salvataggio_prenotazioni_" + u.getMatricola() + ".txt"));
                Iterator<Prenotazione> iter = db.iteratorPrenotazioni();
                while(iter.hasNext()) {
                    Prenotazione p = iter.next();
                    Utente utente = p.getUtente();
                    bw.write(p.getCodicePrenotazione() + ";" + utente.getNome() + ";" + utente.getCognome() + ";" + utente.getMatricola() + ";" + p.getCodicePrenotazione() + ";" + p.isValida() + ";");
                    bw.newLine();
                }
                bw.close();
            } catch (IOException ex) {
                Logger.getLogger(GUI_RimozionePrenotazione.class.getName()).log(Level.SEVERE, null, ex);
            }
            jTextArea1.setText("Salvataggio su file svolto correttamente.");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

